const sumar = (num1,num2)=>{
    return parseInt(num1) + parseInt(num2);
}
const restar = (num1,num2)=>{
    return parseInt(num1) - parseInt(num2); 
}
const multiplicar = (num1,num2)=>{
    return parseInt(num1) * parseInt(num2); 
}
const dividir = (num1,num2)=>{
    return parseInt(num1) / parseInt(num2); 
}

